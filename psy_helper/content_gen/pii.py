"""PII-фильтр для черновиков контента.

ЦЕЛЬ: предупредить ревьюера (Анну), что в драфте есть подозрительные
имена / телефоны / email — могут быть утечки реальных клиентов.

Метод: regex + список known_names. БЕЗ LLM (быстро, дёшево, прозрачно).
Результат — флаги, НЕ блок. Решение оставляем человеку.

Использование:
    flags = detect_pii(draft_text)
    if flags:
        log.warning("pii_in_draft", flags=flags)
        # В UI: красная плашка «Найдены: ...».
"""
from __future__ import annotations

import re

# Common Russian first names. Не исчерпывающий, но покрывает топ-200.
# Цель — не «найти все имена», а пометить подозрительное для ручной проверки.
_COMMON_NAMES_RU = {
    # Женские
    "Анна", "Маша", "Мария", "Катя", "Екатерина", "Оксана", "Ольга", "Лена",
    "Елена", "Наташа", "Наталья", "Света", "Светлана", "Юля", "Юлия", "Ира",
    "Ирина", "Даша", "Дарья", "Настя", "Анастасия", "Полина", "Алина",
    "Кристина", "Вика", "Виктория", "Алёна", "Алена", "Соня", "София",
    "Любовь", "Люба", "Лиза", "Елизавета", "Карина", "Валерия", "Лера",
    "Ангелина", "Ксения", "Ксюша", "Татьяна", "Таня", "Жанна", "Алла",
    "Галина", "Галя", "Тамара", "Зоя", "Раиса", "Нина", "Вера", "Надежда",
    "Надя", "Любовь", "Маргарита", "Рита",
    # Мужские
    "Андрей", "Алексей", "Лёша", "Леша", "Дима", "Дмитрий", "Саша",
    "Александр", "Сергей", "Серёжа", "Серега", "Иван", "Ваня", "Миша",
    "Михаил", "Володя", "Владимир", "Костя", "Константин", "Паша", "Павел",
    "Слава", "Вячеслав", "Игорь", "Олег", "Виктор", "Витя", "Юра", "Юрий",
    "Гриша", "Григорий", "Денис", "Антон", "Артём", "Артем", "Никита",
    "Кирилл", "Максим", "Макс", "Илья", "Боря", "Борис", "Роман", "Рома",
    "Стас", "Станислав", "Тимур", "Феликс", "Эдуард", "Эдик",
}


# Не PII — это публичные/собирательные/типовые имена, которые часто
# встречаются в материалах. Не фладжим их.
_WHITELIST_PUBLIC = {
    "Анна",     # автор системы (но если фамилия рядом — отдельная история)
    "Оксана",   # соавтор Академии
}


_PHONE_RE = re.compile(
    r"(?<!\d)(\+?\d[\s\-()]*){10,14}(?!\d)"
)

_EMAIL_RE = re.compile(
    r"\b[\w.+-]+@[\w-]+\.[\w.-]+\b"
)

# Возможные имена: 1 заглавная + 2-15 строчных кириллических букв.
# Не строгий грамматический разбор — просто кандидаты для сверки со словарём.
_NAME_CANDIDATE_RE = re.compile(r"\b([А-ЯЁ][а-яё]{2,15})\b")


def detect_pii(text: str, *, allow_names: set[str] | None = None) -> list[str]:
    """Вернуть список флагов вида `["name:Маша", "phone:+7...", "email:..."]`.

    `allow_names` — дополнительный whitelist (например, исторические фигуры,
    о которых пишет Анна в материалах).
    """
    flags: list[str] = []
    allow = (allow_names or set()) | _WHITELIST_PUBLIC

    for m in _PHONE_RE.finditer(text):
        raw = m.group(0).strip()
        # отсекаем годы / номера лекций
        digits = re.sub(r"\D", "", raw)
        if len(digits) >= 10:
            flags.append(f"phone:{raw}")

    for m in _EMAIL_RE.finditer(text):
        flags.append(f"email:{m.group(0)}")

    seen_names: set[str] = set()
    for m in _NAME_CANDIDATE_RE.finditer(text):
        cand = m.group(1)
        if cand in _COMMON_NAMES_RU and cand not in allow and cand not in seen_names:
            flags.append(f"name:{cand}")
            seen_names.add(cand)

    return flags
